package configuration;


public class Configuration {
    private int port;
    private String host;

    //    Port Numbers
    public int getPort() {
        return port;
    }

    public void setPort(int port) {
        this.port = port;
    }

    public void setHost(String host) {
        this.host = host;
    }

    // IP address
    public String getHost() {
        return host;
    }


}